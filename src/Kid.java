public class Kid {

    String kidName;
    String element;

    public Kid (String kidName, String element) {
        this.kidName = kidName;
        this.element = element;
    }

    @Override
    public String toString() {
        return "Kid{" +
                "kidName='" + kidName + '\'' +
                ", element='" + element + '\'' +
                '}';
    }

    public String getElement() {
        return getElement();
    }
}
